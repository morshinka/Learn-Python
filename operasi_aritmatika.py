#opreasi aritmatika

a = 5
b = 10

#penjumlahan
hasil = a + b
print (a, "+", b, "=", hasil)

#pengurangan
kurang = a - b
print (a, "-", b, "=", kurang)

#perkalian
kali = a * b
print (a, "*", b, "=", kali)

#pembagian
bagi = b / a
print (a, "/", b, "=", bagi)

#operasi exponen (pangkat)
pangkat = b ** a
print (a, "**", b, "=", pangkat)

#opreasi modulus (sisa pembagian)
var_a = 11
var_b = 2
mod = var_a % var_b
print (a, "%", b, "=", mod)

#opreasi floor division (akan dibulatkan ke bawah) 
var_a = 13
var_b = 2
mod = var_a // var_b
print (a, "//", b, "=", mod)

#prioritas
#data dalam kurung > pangkat > perkalian > penjumlahan > pengurangan